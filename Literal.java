        System.out.println(13%-3);
        System.out.println(0x21);// heaxadecimal literal are deonted with the leading 0x or 0X
        System.out.println(0713);// in java ocatl values are denoted with the leading zero
        System.out.println(0b1010); // binary literals are denoted with the leading 0b
        System.out.println(123_456_789);// is same as 123,456,789 compiler igored the
        boolean a = true;
        System.out.println(a);// Boolean literals are simple. There are only two logical values that a boolean value can have, true and false.





